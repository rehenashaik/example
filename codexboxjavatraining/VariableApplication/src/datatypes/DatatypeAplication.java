package datatypes;

public class DatatypeAplication
{
    int num =10;
    float marks = 90.5f;
    char grade = 'A';
    String name = "rehana";
    long phno = 6302802971l;
    byte rollnum = 52;
    boolean flag = true;
    short id = 250;
    double value = 7000.534;
    public static void main(String[] args)
    {

        DatatypeAplication dta = new DatatypeAplication();
        System.out.println("STUDENT num ="+dta.num);
        System.out.println("student marks ="+dta.marks);
        System.out.println("student grade ="+dta.grade);
        System.out.println("student name ="+dta.name);
        System.out.println("student phno ="+dta.phno);
        System.out.println("student rollnum ="+dta.rollnum);
        System.out.println("student flag="+dta.flag);
        System.out.println("student id="+dta.id);
        System.out.println("student value="+dta.value);




    }
}
