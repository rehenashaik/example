package operators;

public class RelationalApplication
{
    public static void main(String[] args)
    {
        int a = 28;
        int b = 50;
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>=b);
        System.out.println(a<=b);

    }
}
