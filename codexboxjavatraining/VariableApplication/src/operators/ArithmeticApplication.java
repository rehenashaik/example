package operators;

public class ArithmeticApplication
{
    public static void main(String[] args)
    {
        int a = 40;
        int b = 80;
        int add = a+b;
        int sub = a-b;
        float div = a/b;
        int mod = a%b;
        int mul = a*b;
        System.out.println("add="+add);
        System.out.println("sub="+sub);
        System.out.println("div="+div);
        System.out.println("mod="+mod);
        System.out.println("mul="+mul);


    }

}